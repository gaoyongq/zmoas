package com.zm.mall.domain.system.coupon;

import java.util.List;

/**
 * EasyUI DataGrid 分页所需json结果对象
 * Created by Bochao on 2017/2/27.
 */
public class EasyUIListResult<T> {
    private Long total;
    private List<T> rows;

    public EasyUIListResult() {
    }

    public EasyUIListResult(Long total, List<T> rows) {
        this.total = total;
        this.rows = rows;
    }

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }

    public List<T> getRows() {
        return rows;
    }

    public void setRows(List<T> rows) {
        this.rows = rows;
    }
}
