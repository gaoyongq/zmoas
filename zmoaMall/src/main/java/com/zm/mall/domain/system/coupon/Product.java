public class Product {
    private String id;
    @JsonProperty("text")
    private String name;

    private Boolean selfSell;//ÊÇ·ñ×ÔÓª

    public Product() {
    }

    public Product(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getSelfSell() {
        return selfSell;
    }

    public void setSelfSell(Boolean selfSell) {
        this.selfSell = selfSell;
    }
}
