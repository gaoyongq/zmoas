package com.zm.mall.domain.system;



public class ApplyCar {
	 private Integer id ;
	 private String applyCarCode;
	 private String smallgroup;
	 private String transactor;
	 private String number;
	 private String state;
	 private String showState;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getApplyCarCode() {
		return applyCarCode;
	}

	public void setApplyCarCode(String applyCarCode) {
		this.applyCarCode = applyCarCode;
	}

	public String getSmallgroup() {
		return smallgroup;
	}

	public void setSmallgroup(String smallgroup) {
		this.smallgroup = smallgroup;
	}

	public String getTransactor() {
		return transactor;
	}

	public void setTransactor(String transactor) {
		this.transactor = transactor;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getShowState() {
		return showState;
	}

	public void setShowState(String showState) {
		this.showState = showState;
	}
}
