package com.zm.mall.domain.business.product;

import java.util.List;

/**
 * EasyUI树形展现产品分类
 * Created by Bochao on 2017/4/7.
 */
public class EasyUICatTreeNode {
    private Long id; //分类Id
    private String text; //分类名称
    private String state; //树节点打开状态
    private boolean checked; //树节点CheckBox选中状态
    private boolean leaf; //是否是叶子节点
    private List<EasyUICatTreeNode> children; //子分类节点

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public boolean isChecked() {
        return checked;
    }

    public void setChecked(boolean checked) {
        this.checked = checked;
    }

    public boolean isLeaf() {
        return leaf;
    }

    public void setLeaf(boolean leaf) {
        this.leaf = leaf;
    }

    public List<EasyUICatTreeNode> getChildren() {
        return children;
    }

    public void setChildren(List<EasyUICatTreeNode> children) {
        this.children = children;
    }
}
