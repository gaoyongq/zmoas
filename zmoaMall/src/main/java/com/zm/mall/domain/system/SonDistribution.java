//物流配送费用报销
public class SonDistribution implements Serializable {
    private Integer id;
    private String sonDistributionCode;//自己编码
    private String distributionCode;//配送编码
    private String  indentCode;//订单编码
    private String amountSC;//实际数量
    private String kilometre;//公里数
    private String consume;//耗油/元/km
    private String remark;//备注


    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getSonDistributionCode() {
        return sonDistributionCode;
    }

    public void setSonDistributionCode(String sonDistributionCode) {
        this.sonDistributionCode = sonDistributionCode;
    }

    public String getDistributionCode() {
        return distributionCode;
    }

    public void setDistributionCode(String distributionCode) {
        this.distributionCode = distributionCode;
    }

    public String getIndentCode() {
        return indentCode;
    }

    public void setIndentCode(String indentCode) {
        this.indentCode = indentCode;
    }

    public String getAmountSC() {
        return amountSC;
    }

    public void setAmountSC(String amountSC) {
        this.amountSC = amountSC;
    }

    public String getKilometre() {
        return kilometre;
    }

    public void setKilometre(String kilometre) {
        this.kilometre = kilometre;
    }

    public String getConsume() {
        return consume;
    }

    public void setConsume(String consume) {
        this.consume = consume;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
